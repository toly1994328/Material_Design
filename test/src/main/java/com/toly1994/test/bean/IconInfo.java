public class IconInfo {
    private int icon;
    private String info;

    public IconInfo(int icon, String info) {
        this.icon = icon;
        this.info = info;
    }

    public int getIcon() {
        return icon;
    }

    public void setIcon(int icon) {
        this.icon = icon;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }
}
